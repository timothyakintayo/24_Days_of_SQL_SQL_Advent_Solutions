-- Table Schema:
-- Table: cocoa_logs
--   log_id: INT
--   break_id: INT
--   cocoa_id: INT
--
-- Table: break_schedule
--   break_id: INT
--   location_id: INT
--
-- Table: cocoa_types
--   cocoa_id: INT
--   cocoa_name: VARCHAR
--
-- Table: locations
--   location_id: INT
--   location_name: VARCHAR
--

-- My Solution:

SELECT *
FROM break_schedule bs
LEFT JOIN cocoa_logs cl
ON bs.break_id = cl.break_id
LEFT JOIN cocoa_types ct
ON ct.cocoa_id = cl.cocoa_id
LEFT JOIN locations ls
ON ls.location_id = bs.location_id
WHERE 
(cl.break_id IS NOT NULL AND ct.cocoa_id IS NOT NULL AND ls.location_id IS NOT NULL)
